package gui;

import java.awt.Color;

public class ConstructionColors {
	private static final Color BACKGROUND_COLOR = Color.WHITE;

	private static final Color BUTTON_BACKGROUND_COLOR = Color.WHITE;
	private static final Color BUTTON_FOREGROUND_COLOR = Color.BLACK;
	private static final Color BUTTON_HIGHLIGHT_COLOR = Color.BLUE;

	private static final Color INTERSECTION_COLOR = Color.BLACK;
	private static final Color LINE_AND_CIRCLE_COLOR = Color.GRAY;

	public static Color getBackgroundColor() {
		return BACKGROUND_COLOR;
	}

	public static Color getButtonBackgroundColor() {
		return BUTTON_BACKGROUND_COLOR;
	}

	public static Color getButtonForegroundColor() {
		return BUTTON_FOREGROUND_COLOR;
	}

	public static Color getButtonHighlightColor() {
		return BUTTON_HIGHLIGHT_COLOR;
	}

	public static Color getIntersectionColor() {
		return INTERSECTION_COLOR;
	}

	public static Color getLineAndCircleColor() {
		return LINE_AND_CIRCLE_COLOR;
	}
}
